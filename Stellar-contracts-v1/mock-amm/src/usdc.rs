use soroban_sdk::{Address, BytesN, Env};

// Network IDs are SHA-256(network passphrase).
const TESTNET_NETWORK_ID: [u8; 32] = [
    0xce, 0xe0, 0x30, 0x2d, 0x59, 0x84, 0x4d, 0x32, 0xbd, 0xca, 0x91, 0x5c, 0x82, 0x03, 0xdd, 0x44,
    0xb3, 0x3f, 0xbb, 0x7e, 0xdc, 0x19, 0x05, 0x1e, 0xa3, 0x7a, 0xbe, 0xdf, 0x28, 0xec, 0xd4, 0x72,
];
const MAINNET_NETWORK_ID: [u8; 32] = [
    0x7a, 0xc3, 0x39, 0x97, 0x54, 0x4e, 0x31, 0x75, 0xd2, 0x66, 0xbd, 0x02, 0x24, 0x39, 0xb2, 0x2c,
    0xdb, 0x16, 0x50, 0x8c, 0x01, 0x16, 0x3f, 0x26, 0xe5, 0xcb, 0x2a, 0x3e, 0x10, 0x45, 0xa9, 0x79,
];

pub const TESTNET_USDC_SAC: &str = "CAQCMV4JFG4EZXQEAV7TUV2E52DMSO2LQKBOSA7UM3B4NIP4DQJ3JHQJ";
pub const MAINNET_USDC_SAC: &str = "CCW67TSZV3SSS2HXMBQ5JFGCKJNXKZM7UQUWUZPUTHXSTZLEO7SJMI75";

pub fn address(env: &Env) -> Address {
    let network_id = env.ledger().network_id();
    let address = if network_id == BytesN::from_array(env, &TESTNET_NETWORK_ID) {
        TESTNET_USDC_SAC
    } else if network_id == BytesN::from_array(env, &MAINNET_NETWORK_ID) {
        MAINNET_USDC_SAC
    } else {
        panic!("unsupported network: no configured USDC SAC");
    };

    Address::from_str(env, address)
}
